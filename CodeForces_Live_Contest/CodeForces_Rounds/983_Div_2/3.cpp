#define int long long

bool solve()
{
    int n;
    cin>>n;
    vector<int>a(n);
    for(int i=0;i<n;++i){
        cin>>a[i];
    }
    sort(a.begin(),a.end());
    int ans=n;
    for(int i=1;i<n;++i){
        int ft=a[i-1];
        int sc=a[i];
        int idx=lower_bound(a.begin(),a.end(),ft+sc)-a.begin();
        ans=min(ans,i-1+n-idx);
    }
    cout<<ans<<endl;
    return true;
}


